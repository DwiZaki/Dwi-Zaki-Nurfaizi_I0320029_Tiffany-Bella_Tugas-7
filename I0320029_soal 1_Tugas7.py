string = "garuda wisnu kencana"

# Capitalize
cap = string.capitalize()
print("string setelah di capitalize: \n", cap)
print()

# Center
cen = string.center(50,'-')
print("string setelah di center: \n", cen)
print()

# Count
x = string.count('a')
print('Jumlah huruf a =', x)
print()

# upper 
up = string.upper()
print('string setelah di upper; \n', up)
