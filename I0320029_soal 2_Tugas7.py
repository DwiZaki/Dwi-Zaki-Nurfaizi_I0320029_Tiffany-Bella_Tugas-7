x = 86.02
y = -21.52

# ceil
import math
print('x = ', x)
print('y = ', y)
print()
print('nilai x ceil = ', math.ceil(x))
print('nilai y ceil = ', math.ceil(y))
print()

# fabs
print('nilai math.fabs x = ', math.fabs(x))
print('nilai math.fabs y = ', math.fabs(y))
print()

# floor
print('nilai math.floor x = ', math.floor(x))
print('nilai math.floor y = ', math.floor(y))
print()

# sqrt
print('nilai akar kuadrat dari x = {:.2f}'.format(math.sqrt(x)))
print('nilai akar kuadrat dari y = {:.2f}'.format(math.sqrt(math.fabs(y))))
print()